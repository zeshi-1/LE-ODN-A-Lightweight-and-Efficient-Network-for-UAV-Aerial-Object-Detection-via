class TextEncoder(nn.Module):
    """ÊñáÊú¨ÁºñÁ†ÅÂô®Ôºå‰ΩøÁî®RoBERTaÂ∞ÜÊñáÊú¨ËΩ¨Êç¢‰∏∫ÁâπÂæÅÂêëÈáèÔºàÂÆåÂÖ®Âõ∫ÂÆö‰∏çËÆ≠ÁªÉÔºâ"""
    def __init__(self, hidden_size=512, max_length=512):
        super().__init__()
        self.hidden_size = hidden_size
        self.max_length = max_length
        
        # Âä†ËΩΩÈ¢ÑËÆ≠ÁªÉÊ®°ÂûãÂíåtokenizer
        self.tokenizer = RobertaTokenizer.from_pretrained('roberta-base')
        self.roberta = RobertaModel.from_pretrained('roberta-base')
        
        # ÂÆåÂÖ®ÂÜªÁªìRoBERTaÊâÄÊúâÂèÇÊï∞Ôºå‰∏çËøõË°å‰ªª‰ΩïËÆ≠ÁªÉ
        for param in self.roberta.parameters():
            param.requires_grad = False
        
        # ÊäïÂΩ±Â±Ç‰πüÂõ∫ÂÆöÔºå‰∏çËÆ≠ÁªÉ
        self.projection = nn.Linear(768, hidden_size)  # RoBERTaËæìÂá∫Áª¥Â∫¶‰∏∫768
        for param in self.projection.parameters():
            param.requires_grad = False
    
    def forward(self, texts):
        """
        Args:
            texts: List[str] ÊñáÊú¨ÂàóË°®
        Returns:
            text_features: Tensor [B, hidden_size, 1, 1]
        """
        # TokenizeÊñáÊú¨
        inputs = self.tokenizer(
            texts, 
            return_tensors='pt', 
            padding=True, 
            truncation=True, 
            max_length=self.max_length
        ).to(next(self.roberta.parameters()).device)
        
        # ‰ΩøÁî®evalÊ®°ÂºèÔºåÁ°Æ‰øù‰∏çËÆ°ÁÆóÊ¢ØÂ∫¶
        self.roberta.eval()
        self.projection.eval()
        
        with torch.no_grad():  # ÂÆåÂÖ®Á¶ÅÁî®Ê¢ØÂ∫¶ËÆ°ÁÆó
            # Ëé∑ÂèñRoBERTaËæìÂá∫
            outputs = self.roberta(**inputs)
            
            # ‰ΩøÁî®[CLS] tokenÁöÑÁâπÂæÅ
            cls_features = outputs.last_hidden_state[:, 0, :]  # [B, 768]
            
            # ÊäïÂΩ±Âà∞ÁõÆÊ†áÁª¥Â∫¶
            text_features = self.projection(cls_features)  # [B, hidden_size]
            
            # ÈáçÂ°ë‰∏∫ÁâπÂæÅÂõæÊ†ºÂºè [B, C, 1, 1]
            text_features = text_features.unsqueeze(-1).unsqueeze(-1)
        
        return text_features

class MMFF_Bottleneck(nn.Module):
    """Â§öÊ®°ÊÄÅÁâπÂæÅËûçÂêàÁöÑBottleneckÊ®°Âùó"""
    def __init__(self, c1, c2, text_dim, shortcut=True, g=1, k=(3, 3), e=0.5, dropout_rate=0.5):
        super().__init__()
        c_ = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, k[0], 1)
        self.cv2 = Conv(c_, c2, k[1], 1, g=g)
        
        # ÊñáÊú¨ÁâπÂæÅÂ§ÑÁêÜÔºàËøô‰∫õÂ±ÇÈúÄË¶ÅËÆ≠ÁªÉÔºâ
        self.text_proj = nn.Conv2d(text_dim, c2, 1, 1)
        self.text_dropout = nn.Dropout2d(dropout_rate)
        
        self.add = shortcut and c1 == c2

    def forward(self, x, text_features=None, use_text=True):
        """
        Args:
            x: ÂõæÂÉèÁâπÂæÅ [B, C, H, W]
            text_features: ÊñáÊú¨ÁâπÂæÅ [B, text_dim, 1, 1]
            use_text: ÊòØÂê¶‰ΩøÁî®ÊñáÊú¨ÁâπÂæÅ
        """
        if text_features is not None and use_text:
            # Â§ÑÁêÜÊñáÊú¨ÁâπÂæÅÔºàtext_featuresÂ∑≤ÁªèÊòØ‰ªéÂÜªÁªìÁöÑTextEncoderËæìÂá∫ÁöÑÔºâ
            text_proj = self.text_proj(text_features)
            # Â∞ÜÊñáÊú¨ÁâπÂæÅÊâ©Â±ïÂà∞‰∏éÂõæÂÉèÁâπÂæÅÁõ∏ÂêåÁöÑÁ©∫Èó¥Áª¥Â∫¶
            text_expanded = text_proj.expand(-1, -1, x.size(2), x.size(3))
            # Â∫îÁî®dropoutÔºàËÆ≠ÁªÉÊó∂ÈöèÊú∫ÈÅÆÊå°ÊñáÊú¨ÁâπÂæÅÔºâ
            text_expanded = self.text_dropout(text_expanded)
        else:
            text_expanded = 0
        
        # ÂõæÂÉèÁâπÂæÅÂ§ÑÁêÜ
        img_features = self.cv2(self.cv1(x))
        
        # Â§öÊ®°ÊÄÅËûçÂêà
        if self.add:
            return x + img_features + text_expanded
        else:
            return img_features + text_expanded

class C2f_MMFF(nn.Module):
    """CSP Bottleneck with 2 convolutions and Multi-Modal Feature Fusion"""
    def __init__(self, c1, c2, n=1, shortcut=False, g=1, e=0.5, text_dim=512, dropout_rate=0.5):
        super().__init__()
        self.c = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, 2 * self.c, 1, 1)
        self.cv2 = Conv((2 + n) * self.c, c2, 1)  # optional act=FReLU(c2)
        
        # Â§öÊ®°ÊÄÅbottleneckÊ®°Âùó
        self.m = nn.ModuleList(
            MMFF_Bottleneck(self.c, self.c, text_dim, shortcut, g, k=((3, 3), (3, 3)), e=1.0, dropout_rate=dropout_rate) 
            for _ in range(n)
        )
        
        # ÊñáÊú¨ÁºñÁ†ÅÂô®ÔºàÂÆåÂÖ®Âõ∫ÂÆö‰∏çËÆ≠ÁªÉÔºâ
        self.text_encoder = TextEncoder(hidden_size=text_dim)
        self.text_dim = text_dim

    def forward(self, x, texts=None, use_text=True):
        """
        Args:
            x: ÂõæÂÉèÁâπÂæÅ [B, C, H, W]
            texts: List[str] ÊñáÊú¨ÊèèËø∞ÂàóË°®
            use_text: ÊòØÂê¶‰ΩøÁî®ÊñáÊú¨ÁâπÂæÅ
        """
        # ÁºñÁ†ÅÊñáÊú¨ÁâπÂæÅÔºà‰ΩøÁî®ÂÆåÂÖ®ÂÜªÁªìÁöÑTextEncoderÔºâ
        if texts is not None and use_text:
            text_features = self.text_encoder(texts)
        else:
            # ÂàõÂª∫Èõ∂ÊñáÊú¨ÁâπÂæÅ
            batch_size = x.size(0)
            text_features = torch.zeros(batch_size, self.text_dim, 1, 1).to(x.device)
        
        # ÂàÜÂâ≤ÂõæÂÉèÁâπÂæÅ
        y = list(self.cv1(x).chunk(2, 1))
        
        # ÈÄöËøáÂ§öÊ®°ÊÄÅbottleneckÂ§ÑÁêÜ
        for m in self.m:
            y.append(m(y[-1], text_features, use_text))
        
        return self.cv2(torch.cat(y, 1))

    def forward_split(self, x, texts=None, use_text=True):
        """Applies spatial attention to module's input."""
        if texts is not None and use_text:
            text_features = self.text_encoder(texts)
        else:
            batch_size = x.size(0)
            text_features = torch.zeros(batch_size, self.text_dim, 1, 1).to(x.device)
            
        y = list(self.cv1(x).split((self.c, self.c), 1))
        y.extend(m(y[-1], text_features, use_text) for m in self.m)
        return self.cv2(torch.cat(y, 1))